package connector;

public class NinjaJsonObject {
	
	private String[] result;
	private String id;
	private int total;
	
	public NinjaJsonObject() {
		
	}

	public String[] getResult() {
		return result;
	}

	public void setResult(String[] result) {
		this.result = result;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getTotal() {
		return total;
	}

	public void setTotal(int total) {
		this.total = total;
	}
	
	

}
